export const initializeConstantsinLocalStorage = () => {
  if (typeof window === 'undefined') return;
  // TODO: database'e tasinacak
  let constantsinLocalStorage = localStorage.getItem('constants');

  if (constantsinLocalStorage) return;

  const informationsStringOnly = informations.map((item) => {
    return { title: item.title, description: item.description };
  });

  const coursesCardStringOnly = coursesCard.map((item) => {
    return { title: item.title, quantity: item.quantity };
  });

  const constants = {
    image,
    info,
    informations: informationsStringOnly,
    coursesCard: coursesCardStringOnly,
    contact,
    categories,
    menus,
    featured,
    instructors,
    clients,
    blogcard,
    logobanner,
    resources,
    footercourses,
  };

  // TODO: database'e tasinacak
  localStorage.setItem('constants', JSON.stringify(constants));

  constantsinLocalStorage = localStorage.getItem('constants');
  if (constantsinLocalStorage) {
    console.log('Constants have been added to localStorage.');
  }
};
